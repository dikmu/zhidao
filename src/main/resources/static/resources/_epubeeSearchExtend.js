//只有当界面上有书籍时，才执行标记
var marked = false;

function tryMark() {
    if (!marked) {
        if ($('.list_title a').length > 0) {
            markAllDownloadedBook();
            marked = true;
        } else {
            setTimeout(tryMark, 1000);
        }
    }
}

function markAllDownloadedBook() {
    var count = 0;
    $('#tryMark')[0].innerHTML = "markAllDownloadedBook";
    var eis = $('.list_title a');
    var req_str = "";
    for (var i = 0; i < eis.length; i++) {
        req_str = req_str + eis[i].innerHTML;//不含扩展名的book title
        if (i < eis.length - 1) {
            req_str = req_str + ":::";
        }
    }


    $.ajax({
        type: "post",
        contentType: "application/json",
        url: "http://localhost:8081/epubee/receive",
        data: req_str,
        dataType: "json",
        success: function (result) {
            var selecteds = result.selected;
            console.log(selecteds);
            for (var i = 0; i < selecteds.length; i++) {
                for (var j = 0; j < eis.length; j++) {
                    //console.log(selecteds[i]+"  ---  "+eis[j].innerHTML);
                    if (selecteds[i] == eis[j].innerHTML) {
                        //console.log("--------------")
                        eis[j].innerHTML = eis[j].innerHTML + "——已下载";
                        $(eis[j]).parent().parent().children()[2].remove();
                        $($(eis[j]).parent().parent().children()[1]).css("color", "red");
                        count++;
                    }
                }
            }

            $('#tryMark')[0].innerHTML = "markAllDownloadedBook(" + count + ")";
        }
    });

}


/**获取鼠标选中的文本，并再后台做模糊匹配**/

function on_mouse_down2(event) {
    mouse_down_x = event.clientX;
    mouse_down_y = event.clientY;
}

function on_mouse_up2(event) {
    if (Math.abs(event.clientX - mouse_down_x) > 2 || Math.abs(event.clientY - mouse_down_y) > 2) {
        var sText = document.selection == undefined ? document.getSelection().toString() : document.selection.createRange().text;
        if (sText != "") {
            if (sText.length > 20)
                sText = sText.substr(0, 20);
        }
        $.ajax({
            type: "get",
            url: "http://localhost:8081/epubee/searchTitle/" + sText,
            dataType: "application/json",
            success: function (result) {
                result = eval("(" + result + ")");
                console.log("fount:" + result.length);
                for (var i = 0; i < result.length; i++) {
                    console.log(result[i]);
                }
            }
        });
    }
}

document.addEventListener("mousedown", on_mouse_down2, true);
document.addEventListener("mouseup", on_mouse_up2, true);

